package com.jspider.ArrayOfObjects;

public class Nanda {
	int sid;
	String sname;
	String loc;
	public Nanda(int sid, String sname, String loc) {
		super();
		this.sid = sid;
		this.sname = sname;
		this.loc = loc;
	}
	@Override
	public String toString() {
		return "Nanda [sid=" + sid + ", sname=" + sname + ", loc=" + loc + "]";
	}
	
	
	
	

}
